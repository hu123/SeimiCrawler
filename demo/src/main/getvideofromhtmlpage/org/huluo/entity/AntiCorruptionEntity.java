package org.huluo.entity;

public class AntiCorruptionEntity {
    private String id;
    private String imageUrl;
    private String title;
    private String onlineVedioUrl;
    private String chatcontent;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOnlineVedioUrl() {
        return onlineVedioUrl;
    }

    public void setOnlineVedioUrl(String onlineVedioUrl) {
        this.onlineVedioUrl = onlineVedioUrl;
    }

    public String getChatcontent() {
        return chatcontent;
    }

    public void setChatcontent(String chatcontent) {
        this.chatcontent = chatcontent;
    }

}
